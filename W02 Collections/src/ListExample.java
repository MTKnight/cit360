import java.util.ArrayList;

/**
 * @author Marci Godfrey
 * CIT 360: Collections Examples: List
 */

public class ListExample {

	public static void main(String[] args) {

		// Declare
		ArrayList<String> myKids = new ArrayList<String>();
		
		//List Data
		myKids.add("Briella");
		myKids.add("Emmaline");
		myKids.add("Claire");
		myKids.add("Adalie");
		
		//Output
		System.out.println("The Godfrey Girls: " + myKids);
		System.exit(1); 

	}

}
